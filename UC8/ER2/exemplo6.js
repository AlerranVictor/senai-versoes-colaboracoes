// for (laço mais usado, por ser simples), porém, apenas permite laços contados (com contador)

for(contador = 0; contador < 100; contador ++){
    console.log(contador)
}

/*
 Incremento
    contador (exemplo: contador++) é o mesmo que contador = contador + 1

 Decremento
    contador (exemplo: contador--) é o mesmo que contador = contador - 1
*/