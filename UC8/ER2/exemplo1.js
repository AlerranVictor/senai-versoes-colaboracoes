/*regras para operadores de comparação
= (utilizado para atribuição)
==  para comparação (verifica igualdade). Ao comparar duas coisas, o resultado é booleano, ou seja, True ou False.
=== para comparação, porém, verifica se é idêntico (mesmo tipo de dado e valor)
!= verifica a diferença
< menor
> maior
<= menor ou igual
>= maior ou igual
*/

/*Operadores de atribuição
= atribuição
+= (x += y) é a mesma coisa que (X = X + y)
-= (X -= y) é a mesma coisa que (x = x - y)
*= (x *= y) é a mesma coisa que (x = x * y)
/= (x /= y) é a mesma coisa que (x = x / y)
*/

/*Operadores lógicos
&& (e)
|| (ou)
! (negação) (muda/inverte o estado lógico em valores booleanos)

Utilizando o operador E (&&)
V && V = V
V && F = F
F && F = F
F && V = F
Posso definir que com o operador E a resposta apenas será verdadeira se ambas as proposições forem verdadeiras

Utilizando operador OU
V || V = V
V || F = V
F || F = F
F || V = V
Psso definir que com o operador OU a resposta apenas será false se ambas as proposições forem falsas
*/